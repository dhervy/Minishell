#include "libft.h"

void	ft_tabfree(char **tab)
{
	int		i;

	i = 0;
	while (tab[i])
	{
		ft_bzero(tab[i], ft_strlen(tab[i]));
		free(tab[i]);
		i++;
	}
	free(tab[i]);
	free(tab);
}
